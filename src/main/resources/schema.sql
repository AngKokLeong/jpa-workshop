DROP TABLE IF EXISTS EMPLOYEE_PROJECTS;
DROP TABLE IF EXISTS PROJECTS;
DROP TABLE IF EXISTS COURSES;
DROP TABLE IF EXISTS DEPARTMENTS;
DROP TABLE IF EXISTS EMPLOYEES;



CREATE TABLE DEPARTMENTS(
    ID          BIGINT          AUTO_INCREMENT      PRIMARY KEY, 
    `NAME`      VARCHAR(255)                        NULL
);


CREATE TABLE EMPLOYEES(
    ID                  BIGINT          AUTO_INCREMENT      PRIMARY KEY,
    `NAME`              VARCHAR(255)    NULL,
    DEPARTMENT_ID       BIGINT          ,
    CONSTRAINT fk_c_department FOREIGN KEY (DEPARTMENT_ID) REFERENCES DEPARTMENTS(ID)       
);


CREATE TABLE COURSES(
    ID                      BIGINT              AUTO_INCREMENT      PRIMARY KEY,
    `NAME`                  VARCHAR(255)    ,
    STARTS                  DATE            ,
    DURATION_IN_MONTHS      FLOAT           ,

    CONSTRAINT fk_c_employee FOREIGN KEY (EMPLOYEE_ID) REFERENCES EMPLOYEES(ID)          
);


CREATE TABLE PROJECTS(
    ID                      BIGINT          AUTO_INCREMENT    PRIMARY KEY,
    `NAME`                  VARCHAR(255)               
);


CREATE TABLE EMPLOYEES_PROJECTS(
    EMPLOYEE_ID             BIGINT,
    PROJECT_ID              BIGINT,

    PRIMARY KEY(EMPLOYEE_ID, PROJECT_ID),
    CONSTRAINT fk_ep_employee FOREIGN KEY (EMPLOYEE_ID) REFERENCES EMPLOYEES(ID),
    CONSTRAINT fk_ep_projects FOREIGN KEY (PROJECT_ID) REFERENCES PROJECTS(ID)
);